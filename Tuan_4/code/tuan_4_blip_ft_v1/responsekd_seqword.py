@torch.no_grad()
def get_pseudo_labels(pil_images):
    inputs = SHARED_PROCESSOR(images=pil_images, return_tensors="pt", padding=True).to(DEVICE)
    inputs["pixel_values"] = inputs["pixel_values"].half()
