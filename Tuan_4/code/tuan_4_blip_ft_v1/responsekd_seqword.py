def load_split_from_zip(zip_path_or_url, split_name):
    """Tải (nếu là URL) + giải nén 1 split -> trả về list metadata với
    image_path cục bộ sẵn sàng để Image.open(). Nếu zip_path_or_url là
    đường dẫn local (vd Kaggle Dataset đã Add Input), truyền thẳng path đó."""
    target_dir = f"data_{split_name}"
    if zip_path_or_url.startswith("http"):
        local_zip = f"{split_name}.zip"
        if not os.path.exists(local_zip):
            print(f"Đang tải {split_name} từ {zip_path_or_url} ...")
            urllib.request.urlretrieve(zip_path_or_url, local_zip)
    else:
        local_zip = zip_path_or_url

    if not os.path.exists(target_dir):
        with zipfile.ZipFile(local_zip, "r") as zf:
            zf.extractall(target_dir)

    with open(os.path.join(target_dir, "metadata.json"), encoding="utf-8") as f:
        metadata = json.load(f)
    for item in metadata:
        item["image_path"] = os.path.join(target_dir, "images", item["image_file"])
    return metadata


