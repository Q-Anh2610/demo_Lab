import matplotlib.pyplot as plt
import random

def visualize_samples(samples, teacher, nokd, seqkd, wordkd, n=5):
    # Chọn ngẫu nhiên n mẫu
    indices = random.sample(range(len(samples)), n)

    for idx in indices:
        item = samples[idx]
        img = Image.open(item['image_path']).convert('RGB')

        # Chuẩn bị input cho các model
        inputs = SHARED_PROCESSOR(images=img, return_tensors='pt').to(DEVICE)

        # Dự đoán từ các model
        with torch.no_grad():
            # Teacher
            t_in = SHARED_PROCESSOR(images=img, return_tensors='pt').to(DEVICE)
            t_in['pixel_values'] = t_in['pixel_values'].half()
            t_ids = teacher.generate(**t_in, max_new_tokens=40)
            t_pred = SHARED_PROCESSOR.decode(t_ids[0], skip_special_tokens=True)

            # Students
            with torch.autocast(device_type='cuda', dtype=torch.float16, enabled=USE_AMP):
                n_ids = nokd.generate(**inputs, max_new_tokens=40)
                s_ids = seqkd.generate(**inputs, max_new_tokens=40)
                w_ids = wordkd.generate(**inputs, max_new_tokens=40)

            n_pred = SHARED_PROCESSOR.decode(n_ids[0], skip_special_tokens=True)
            s_pred = SHARED_PROCESSOR.decode(s_ids[0], skip_special_tokens=True)
            w_pred = SHARED_PROCESSOR.decode(w_ids[0], skip_special_tokens=True)

        # Hiển thị
        plt.figure(figsize=(6, 4))
        plt.imshow(img)
        plt.axis('off')
        plt.show()

        print(f"Ground Truth: {item['caption']}")
        print(f"Teacher (FT): {t_pred}")
        print(f"No-KD (P4):   {n_pred}")
        print(f"Seq-KD (P5):  {s_pred}")
        print(f"Word-KD (P6): {w_pred}")
        print('-' * 80)

