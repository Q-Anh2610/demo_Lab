        if not batch:
            continue
        pil_imgs = [Image.open(b["image_path"]).convert("RGB") for b in batch]
        gt_caps  = [b["caption"] for b in batch]
        gt_enc = SHARED_PROCESSOR(images=pil_imgs, text=gt_caps, return_tensors="pt",
                                   padding="max_length", max_length=MAX_LEN, truncation=True).to(DEVICE)
        gt_labels = make_labels(gt_enc["input_ids"])

        with torch.no_grad():
            teacher_att = get_attention_map(
                get_last_hidden_states(teacher_model, gt_enc["pixel_values"].half()).float()
            )

        optimizer_at.zero_grad()
        with torch.autocast(device_type="cuda", dtype=torch.float16, enabled=USE_AMP and DEVICE == "cuda"):
            student_hidden_states = get_last_hidden_states(student_model_at, gt_enc["pixel_values"])
            student_att = get_attention_map(student_hidden_states.float())
            loss_at = F.mse_loss(student_att, teacher_att)

