# PHẦN 6 — FEATURE-KD: ATTENTION TRANSFER (Zagoruyko & Komodakis, 2017)
# =====================================================================
# Khác FitNets: so sánh SPATIAL ATTENTION MAP (1 giá trị/patch, tổng bình
# phương activation theo chiều channel rồi chuẩn hoá L2) thay vì pooled
# feature -> map có shape [B, N_patches] KHÔNG phụ thuộc số channel, nên
# KHÔNG cần Adapter (BLIP-large/BLIP-base cùng patch grid vì dùng chung
# SHARED_PROCESSOR để resize ảnh).

def get_attention_map(hidden_states, eps=1e-6):
    """hidden_states: [B, N, C] (patch tokens, kể cả CLS) -> attention map [B, N]"""
    att = hidden_states.pow(2).sum(dim=-1)          # tổng bình phương theo channel
    att = F.normalize(att, p=2, dim=1, eps=eps)      # chuẩn hoá L2 theo chiều spatial
    return att


def get_last_hidden_states(model, pixel_values):
    vision_out = model.vision_model(pixel_values=pixel_values, output_hidden_states=True)
    return vision_out.hidden_states[-1]   # [B, N, C] của layer cuối


