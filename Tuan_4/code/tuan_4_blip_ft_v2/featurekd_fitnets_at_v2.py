def get_attention_map(hidden_states, eps=1e-6):
    att = hidden_states.pow(2).sum(dim=-1)
    return F.normalize(att, p=2, dim=1, eps=eps)

def get_last_hidden_states(model, pixel_values):
    vision_out = model.vision_model(pixel_values=pixel_values, output_hidden_states=True)
    return vision_out.hidden_states[-1]

student_model_at = BlipForConditionalGeneration.from_pretrained("Salesforce/blip-image-captioning-base").to(DEVICE)
optimizer_at = AdamW(student_model_at.parameters(), lr=LR, weight_decay=0.01)
scheduler_at = get_linear_schedule_with_warmup(optimizer_at, num_warmup_steps=int(feat_steps * WARMUP_RATIO), num_training_steps=feat_steps)
scaler_at = torch.amp.GradScaler('cuda', enabled=USE_AMP and DEVICE == "cuda")

print(f"Training Attention Transfer (batch_size={BATCH_SIZE_FEAT})...")
best_val_loss_at = float('inf'); best_at_path = "best_student_at.pth"

def validate_at():
    student_model_at.eval()
    val_losses = []
    with torch.no_grad():
        for s_v in range(0, len(fashion_val), BATCH_SIZE_FEAT):
            b_v = fashion_val[s_v:s_v + BATCH_SIZE_FEAT]
            if not b_v: continue
            p_i = [Image.open(b["image_path"]).convert("RGB") for b in b_v]
            g_e = SHARED_PROCESSOR(images=p_i, text=[b["caption"] for b in b_v], return_tensors="pt", padding="max_length", max_length=MAX_LEN, truncation=True).to(DEVICE)
            t_a = get_attention_map(get_last_hidden_states(teacher_model, g_e["pixel_values"].half()).float())
            with torch.autocast(device_type="cuda", dtype=torch.float16, enabled=USE_AMP and DEVICE == "cuda"):
                s_a = get_attention_map(get_last_hidden_states(student_model_at, g_e["pixel_values"]).float())
                l_a_v = F.mse_loss(s_a, t_a)
                o_v = student_model_at(**g_e, labels=make_labels(g_e["input_ids"]))
                l_v = (1 - ALPHA_AT) * o_v.loss + ALPHA_AT * l_a_v
            val_losses.append(l_v.item())
    student_model_at.train()
    return sum(val_losses) / len(val_losses)

