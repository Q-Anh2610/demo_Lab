def validate_fitnets():
    student_model_fitnets.eval(); adapter_fitnets.eval()
    val_losses = []
    with torch.no_grad():
        for s_v in range(0, len(fashion_val), BATCH_SIZE_FEAT):
            b_v = fashion_val[s_v:s_v + BATCH_SIZE_FEAT]
            if not b_v: continue
            p_i = [Image.open(b["image_path"]).convert("RGB") for b in b_v]
            g_c = [b["caption"] for b in b_v]
            g_e = SHARED_PROCESSOR(images=p_i, text=g_c, return_tensors="pt", padding="max_length", max_length=MAX_LEN, truncation=True).to(DEVICE)
            t_f = get_pooled_image_feature(teacher_model, g_e["pixel_values"].half()).float()
            with torch.autocast(device_type="cuda", dtype=torch.float16, enabled=USE_AMP and DEVICE == "cuda"):
                s_f = get_pooled_image_feature(student_model_fitnets, g_e["pixel_values"])
                a_f = adapter_fitnets(s_f)
                l_f = F.mse_loss(a_f.float(), t_f)
                o_g = student_model_fitnets(**g_e, labels=make_labels(g_e["input_ids"]))
                l_v = (1 - ALPHA_FEAT) * o_g.loss + ALPHA_FEAT * l_f
            val_losses.append(l_v.item())
    student_model_fitnets.train(); adapter_fitnets.train()
    return sum(val_losses) / len(val_losses)

