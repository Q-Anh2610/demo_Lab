    student_model_at.train(); epoch_losses = []
    for start in range(0, len(fashion_train), BATCH_SIZE_FEAT):
        batch = fashion_train[start:start + BATCH_SIZE_FEAT]
        if not batch: continue
        pil_imgs = [Image.open(b["image_path"]).convert("RGB") for b in batch]
        gt_caps  = [b["caption"] for b in batch]
        gt_enc = SHARED_PROCESSOR(images=pil_imgs, text=gt_caps, return_tensors="pt", padding="max_length", max_length=MAX_LEN, truncation=True).to(DEVICE)
        with torch.no_grad(): t_att = get_attention_map(get_last_hidden_states(teacher_model, gt_enc["pixel_values"].half()).float())

        optimizer_at.zero_grad()
        with torch.autocast(device_type="cuda", dtype=torch.float16, enabled=USE_AMP and DEVICE == "cuda"):
            s_h = get_last_hidden_states(student_model_at, gt_enc["pixel_values"])
            s_att = get_attention_map(s_h.float())
            l_at = F.mse_loss(s_att, t_att)
            out = student_model_at(**gt_enc, labels=make_labels(gt_enc["input_ids"]))
            loss = (1 - ALPHA_AT) * out.loss + ALPHA_AT * l_at
        scaler_at.scale(loss).backward(); scaler_at.unscale_(optimizer_at)
        torch.nn.utils.clip_grad_norm_(student_model_at.parameters(), 1.0)
        scaler_at.step(optimizer_at); scaler_at.update(); scheduler_at.step()
        epoch_losses.append(loss.item())
        global_step_at += 1

        if global_step_at % check_every_at == 0:
            v_l = validate_at()
            print(f"   [AT] step {global_step_at} (epoch {epoch+1}) | val={v_l:.4f}")
            if v_l < best_val_loss_at:
                best_val_loss_at = v_l
                torch.save(student_model_at.state_dict(), best_at_path)
                print("      --> Lưu checkpoint AT tốt nhất")

    print(f"[AT] Epoch {epoch+1}/{NUM_EPOCHS} | loss={sum(epoch_losses)/len(epoch_losses):.4f}")

